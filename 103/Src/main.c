ctrlmodetype Check_mode(void)
{
	if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(GPIOC,GPIO_PIN_3))
	{
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_4,GPIO_PIN_SET);
		uDelay(10);
		if(GPIO_PIN_SET == HAL_GPIO_ReadPin(GPIOC,GPIO_PIN_3))
		{
			return test_mode;
		}
	}
	else if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(GPIOC,GPIO_PIN_10))
	{
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_4,GPIO_PIN_SET);
		uDelay(10);
		if(GPIO_PIN_SET == HAL_GPIO_ReadPin(GPIOC,GPIO_PIN_10))
		{
			return bt_mode;
		}
	}
		return test_mode;
}
	
